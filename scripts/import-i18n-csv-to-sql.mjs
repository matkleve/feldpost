import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const repoRoot = process.cwd();
const csvPath = join(repoRoot, "docs", "i18n", "translation-workbench.csv");
const outPath = join(repoRoot, "supabase", "seed_i18n.sql");

function parseCsv(text) {
  const rows = [];
  let current = "";
  let row = [];
  let inQuotes = false;

  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    const next = text[i + 1];

    if (ch === '"') {
      if (inQuotes && next === '"') {
        current += '"';
        i++;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (ch === "," && !inQuotes) {
      row.push(current);
      current = "";
      continue;
    }

    if ((ch === "\n" || ch === "\r") && !inQuotes) {
      if (ch === "\r" && next === "\n") i++;
      if (current.length > 0 || row.length > 0) {
        row.push(current);
        rows.push(row);
        row = [];
        current = "";
      }
      continue;
    }

    current += ch;
  }

  if (current.length > 0 || row.length > 0) {
    row.push(current);
    rows.push(row);
  }

  return rows;
}

function escapeSql(value) {
  return String(value ?? "").replace(/'/g, "''");
}

const csv = readFileSync(csvPath, "utf8");
const rows = parseCsv(csv);
if (rows.length < 2) {
  throw new Error("translation-workbench.csv is empty.");
}

const header = rows[0].map((h, idx) => {
  const raw = String(h);
  const withoutBom = idx === 0 ? raw.replace(/^\ufeff/, "") : raw;
  return withoutBom.trim();
});
const keyIdx = header.indexOf("key");
const originalIdx = header.indexOf("original");
const contextIdx = header.indexOf("context");
const enIdx = header.indexOf("en");
const deIdx = header.indexOf("de");
const itIdx = header.indexOf("it");

if (
  [keyIdx, originalIdx, contextIdx, enIdx, deIdx, itIdx].some(
    (idx) => idx === -1,
  )
) {
  throw new Error(
    "translation-workbench.csv must contain key, original, context, en, de, it columns.",
  );
}

const body = rows.slice(1).filter((r) => r.length >= header.length);

const lines = [];
lines.push("-- Generated by scripts/import-i18n-csv-to-sql.mjs");
lines.push("-- Source: docs/i18n/translation-workbench.csv");
lines.push("begin;");
lines.push("");

for (const row of body) {
  const key = (row[keyIdx] ?? "").trim();
  const original = (row[originalIdx] ?? "").trim();
  const context = (row[contextIdx] ?? "").trim();
  const en = (row[enIdx] ?? "").trim();
  const de = (row[deIdx] ?? "").trim();
  const it = (row[itIdx] ?? "").trim();

  if (!key || !original) continue;

  const sourceText = en || original;

  lines.push(
    `insert into public.app_texts (organization_id, key, source_text, source_lang, context)`,
  );
  lines.push(
    `values (null, '${escapeSql(key)}', '${escapeSql(sourceText)}', 'en', '${escapeSql(context)}')`,
  );
  lines.push("on conflict (scope_key) do update set");
  lines.push("  source_text = excluded.source_text,");
  lines.push("  source_lang = excluded.source_lang,");
  lines.push("  context = excluded.context;");
  lines.push("");

  lines.push(
    "insert into public.app_text_translations (app_text_id, lang, translated_text, status)",
  );
  lines.push(`select t.id, 'en', '${escapeSql(en || original)}', 'published'`);
  lines.push("from public.app_texts t");
  lines.push(`where t.organization_id is null and t.key = '${escapeSql(key)}'`);
  lines.push("on conflict (app_text_id, lang) do update set");
  lines.push("  translated_text = excluded.translated_text,");
  lines.push("  status = 'published';");
  lines.push("");

  lines.push(
    "insert into public.app_text_translations (app_text_id, lang, translated_text, status)",
  );
  lines.push(`select t.id, 'de', '${escapeSql(de || original)}', 'published'`);
  lines.push("from public.app_texts t");
  lines.push(`where t.organization_id is null and t.key = '${escapeSql(key)}'`);
  lines.push("on conflict (app_text_id, lang) do update set");
  lines.push("  translated_text = excluded.translated_text,");
  lines.push("  status = 'published';");
  lines.push("");

  lines.push(
    "insert into public.app_text_translations (app_text_id, lang, translated_text, status)",
  );
  lines.push(
    `select t.id, 'it', '${escapeSql(it || en || original)}', 'published'`,
  );
  lines.push("from public.app_texts t");
  lines.push(`where t.organization_id is null and t.key = '${escapeSql(key)}'`);
  lines.push("on conflict (app_text_id, lang) do update set");
  lines.push("  translated_text = excluded.translated_text,");
  lines.push("  status = 'published';");
  lines.push("");
}

lines.push("commit;");
lines.push("");

writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(`Generated SQL seed -> ${outPath}`);
